import streamlit as st
import reveal_slides as rs

def presentation_page():
    st.title("Презентация проекта")
    
    markdown=("""
    ## Прогнозирование отказов оборудования
    
    ### Цель проекта
    Разработка системы предсказания отказов промышленного оборудования на основе датчиков.
    
    ---
    
    ###  Описание датасета
 Датасет содержит синтетические данные, моделирующие задачу предиктивного обслуживания оборудования. Датасет состоит из 10 000 записей, каждая из которых описывает состояние оборудования и включает 14 признаков.
    ---
    ###  Детализация данных:
 1. Product ID:
 Состоит из буквы (L, M, H) и серийного номера.
 2. Air temperature [K]:
 Температура окружающей среды
 3. Process temperature [K]:
 Рабочая температура процесса
 4. Rotational speed [rpm]:
 Скорость вращения
 С добавлением нормально распределенного шума.
 5. Torque [Nm]:
 Крутящий момент
 6. Tool wear [min]:
 Износ инструмента.

    ---
    ### Целевая переменная:
    Machine failure: Бинарная метка (0 или 1), где:
    - 0: Отказ оборудования не произошел. 
    - 1: Отказ оборудования произошел 
    - (хотя бы один из типов отказов активирован).            

    ---
    ### Используемые технологии
    - Python
    - Streamlit (веб-интерфейс)
    - Scikit-learn (машинное обучение)
    - Logistic Regression
    - Random Forest 
    - XGBoost (градиентный бустинг)
    - Pandas (анализ данных)
    
    ---
    
    ### Этапы работы
    1. Загрузка и предобработка данных
    2. Разделение данных на обучающую и тестовую выборки
    3. Обучение моделей машинного обучения
    4. Оценка и выбор лучшей модели
    5. Написание приложения и презентации
    
    ---

    ### Streamlit-приложение 
    - Основная страница: анализ данных и предсказания.
    - Страница с презентацией: описание проекта.            

    ---
    
    ### Заключение
    - Точность предсказаний: до 95%
    - Возможность прогнозировать отказы за 24-48 часов
    - Удобный интерфейс для операторов
    
    ---
    
    ### Демонстрация
    Перейдите на вкладку "Анализ и модель" чтобы:
    - Загрузить свои данные
    - Обучить модель
    - Получить прогнозы
    """)

    # Настройки презентации
    with st.sidebar:
        st.header("Настройки презентации")
        theme = st.selectbox("Тема", 
                           ["black", "white", "league", "beige", 
                            "sky", "night", "serif", "simple", "solarized"])
        height = st.number_input("Высота слайдов", min_value=300, max_value=1000, value=500)
        transition = st.selectbox("Переход", 
                                ["slide", "convex", "concave", "zoom", "none"])
        plugins = st.multiselect("Плагины", 
                               ["highlight", "katex", "mathjax2", 
                                "mathjax3", "notes", "search", "zoom"], 
                               ["highlight", "notes", "zoom"])

    # Отображение презентации
    rs.slides(
        markdown,
        height=height,
        theme=theme,
        config={
            "transition": transition,
            "plugins": plugins,
            "controls": True,
            "progress": True,
        },
        markdown_props={
            "data-separator": "^---$",
            "data-separator-vertical": "^--$"
        },
    )